        // 检查是否是误报组合
        function isFalsePositive(text, match) {
            const lowerText = text.toLowerCase();
            for (const pattern of falsePositivePatterns) {
                if (lowerText.includes(pattern.toLowerCase()) && pattern.toLowerCase().includes(match.toLowerCase())) {
                    return true;
                }
            }
            return false;
        }

        // 敏感词检测
        function checkSensitive() {
            const input = document.getElementById('sensitiveInput').value;
            const resultDiv = document.getElementById('sensitiveResult');
            
            if (!input.trim()) {
                resultDiv.style.display = 'block';
                resultDiv.innerHTML = '<span class="warning">请输入要检测的文字</span>';
                return;
            }

            const foundHigh = [];
            const foundLow = [];
            let highlightedText = input;

            // 检测高置信度敏感词
            sensitiveWordsHigh.forEach(word => {
                const regex = new RegExp(`\\b(${word})\\b`, 'gi');
                const cnRegex = new RegExp(`(${word})`, 'gi');
                if (regex.test(input) || cnRegex.test(input)) {
                    if (!isFalsePositive(input, word)) {
                        foundHigh.push(word);
                    }
                }
            });

            // 检测低置信度敏感词
            sensitiveWordsLow.forEach(word => {
                const regex = new RegExp(`\\b(${word})\\b`, 'gi');
                const cnRegex = new RegExp(`(${word})`, 'gi');
                if (regex.test(input) || cnRegex.test(input)) {
                    if (!isFalsePositive(input, word)) {
                        foundLow.push(word);
                    }
                }
            });

            // 去重
            const uniqueHigh = [...new Set(foundHigh)];
            const uniqueLow = [...new Set(foundLow)];

            // 高亮显示
            uniqueLow.forEach(word => {
                const regex = new RegExp(`(${word})`, 'gi');
                highlightedText = highlightedText.replace(regex, '<span class="sensitive-low">$1</span>');
            });

            uniqueHigh.forEach(word => {
                const regex = new RegExp(`(${word})`, 'gi');
                highlightedText = highlightedText.replace(regex, '<span class="sensitive-high">$1</span>');
            });

            resultDiv.style.display = 'block';
            
            if (uniqueHigh.length > 0 || uniqueLow.length > 0) {
                let html = '<span class="warning">⚠️ 检测到敏感词：</span><br><br>';
                html += '<div class="highlighted-text">' + highlightedText + '</div>';
                html += '<br><strong>🔴 高置信度：</strong> ' + (uniqueHigh.length > 0 ? uniqueHigh.join(', ') : '无');
                html += '<br><strong>🟡 可能误报：</strong> ' + (uniqueLow.length > 0 ? uniqueLow.join(', ') : '无');
                html += '<br><br><small>💡 提示：黄色标记的词可能是正常内容，请结合上下文判断</small>';
                resultDiv.innerHTML = html;
            } else {
                resultDiv.innerHTML = '<span class="safe">✅ 未检测到常见敏感词</span><br><br><div class="highlighted-text">' + input + '</div>';
            }
        }

