// 从 lore-data.js 动态构建特殊链接正则和资料数据

window.LoreData = {
    // 从 lore-data.js 引用数据
    organizations: window.LORE_ORGANIZATIONS || [],
    regions: window.LORE_REGIONS || [],
    characters: window.LORE_CHARACTERS || [],
    
    // 查找方法
    find: function(type, name) {
        const map = {
            organization: this.organizations,
            region: this.regions,
            character: this.characters
        };
        const list = map[type] || [];
        return list.find(item => 
            item.name === name || 
            (item.alias && item.alias.includes(name)) ||
            (item.alias && name.includes(item.alias.split('/')[0]))
        );
    },
    
    // 构建正则（转义特殊字符）
    escapeRegex: function(str) {
        return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    },
    
    buildPatterns: function() {
        const orgs = this.organizations.map(o => this.escapeRegex(o.name)).join('|');
        const regions = this.regions.map(r => this.escapeRegex(r.name)).join('|');
        const chars = this.characters.map(c => this.escapeRegex(c.name)).join('|');
        
        return {
            organization: new RegExp(`\\[(${orgs})\\]`, 'g'),
            region: new RegExp(`\\[(${regions})\\]`, 'g'),
            character: new RegExp(`\\[(${chars})\\]`, 'g')
        };
    },
    
    // 格式化资料用于弹窗显示
    formatLore: function(type, data) {
        if (!data) return '资料加载中...';
        
        let html = `<h3>${data.name}</h3>`;
        if (data.alias) html += `<p><strong>别名：</strong>${data.alias}</p>`;
        if (data.description) html += `<p><strong>描述：</strong>${data.description}</p>`;
        if (data.duties) html += `<p><strong>职责：</strong>${data.duties}</p>`;
        if (data.features) html += `<p><strong>特征：</strong>${data.features}</p>`;
        if (data.role) html += `<p><strong>身份：</strong>${data.role}</p>`;
        if (data.knownMembers) html += `<p><strong>成员：</strong>${data.knownMembers}</p>`;
        if (data.knownAssociates) html += `<p><strong>关联：</strong>${data.knownAssociates}</p>`;
        if (data.location) html += `<p><strong>位置：</strong>${data.location}</p>`;
        if (data.knownLocations) html += `<p><strong>地点：</strong>${data.knownLocations}</p>`;
        if (data.notes) html += `<p><strong>备注：</strong>${data.notes}</p>`;
        if (data.features) html += `<p><strong>特征：</strong>${data.features}</p>`;
        if (data.status) html += `<p><strong>状态：</strong>${data.status}</p>`;
        
        return html;
    }
};

// 动态生成特殊链接正则
window.SPECIAL_LINK_PATTERNS = window.LoreData.buildPatterns();

// 链接解析函数
window.resolveLoreLink = function(matchedText, type) {
    const name = matchedText.slice(1, -1); // 去掉 []
    return window.LoreData.find(type, name);
};

